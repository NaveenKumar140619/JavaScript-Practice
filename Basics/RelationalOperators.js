// Relational operators can help to judge the statement is correct or not, it means true or false check 
//below is the examples for Relational Expessions

//Greater Than
console.log(3 > 4);

//Lesser Than
console.log(3 < 4);

//Greater Than or equal to
console.log(4 >= 5);

//Lesser Than or equal to
console.log(4 <= 5);

// is equals to
console.log(4 === 4);  

//Not equals to
console.log(4 !== 5);

