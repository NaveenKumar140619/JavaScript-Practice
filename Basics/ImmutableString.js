// String Immutable 
/* Individual characters of String, literal cannot be changed
String values or Immutable - the cannot be altered once created */

let str = 'Naveen';
str[4] = 'i'; //not allowed
console.log(str);

/* can re assign the String value to change */

let str1 = "GooGle.com";
str1 = 'GooGle-com'; //re assigned to change allowed
console.log(str1);